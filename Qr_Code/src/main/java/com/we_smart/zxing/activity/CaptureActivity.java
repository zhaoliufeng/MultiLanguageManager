    @Override
    protected void onActivityResult(final int requestCode, int resultCode, Intent data) {
        if (resultCode == RESULT_OK) {
            Log.i("TestData", "requestCode==" + requestCode + ",resultCode==" + resultCode);
            switch (requestCode) {
                case REQUEST_CODE_SCAN_GALLERY:
                    mPhoto_path = "";
                    //获取选中图片的路径
                    String[] proj = { MediaStore.Images.Media.DATA };
                    // 获取选中图片的路径
                    Cursor cursor = getContentResolver().query(data.getData(),
                            proj, null, null, null);

                    if (cursor.moveToFirst()) {

                        int column_index = cursor.getColumnIndexOrThrow(MediaStore.Images.Media.DATA);
                        mPhoto_path = cursor.getString(column_index);
                        if (mPhoto_path == null) {
                            mPhoto_path = getPath(this,
                                    data.getData());
                        }
                    }
                    cursor.close();

                    new Thread(new Runnable() {
                        @Override
                        public void run() {
                            Result result = scanningImage(mPhoto_path);
                            if (result != null) {
                                Intent resultIntent = new Intent();
                                resultIntent.putExtra(RES_TEXT, result.getText());
                                resultIntent.putExtra(RES_FORMAT, result.getBarcodeFormat().ordinal());
                                setResult(RESULT_OK, resultIntent);
                                finish();
                            } else {
                                Message m = handler.obtainMessage();
                                m.what = CaptureActivityHandler.decode_failed;
                                m.obj = "Scan failed!";
                                handler.sendMessage(m);
                            }
                        }
                    }).start();
                    break;
            }
        }
    }

    /**
     * 扫描二维码图片的方法
     *
     * @param path
     * @return
     */
    public Result scanningImage(String path) {
        if (TextUtils.isEmpty(path)) {
            return null;
        }
        Hashtable<DecodeHintType, String> hints = new Hashtable<>();
        hints.put(DecodeHintType.CHARACTER_SET, "UTF8"); //设置二维码内容的编码

        BitmapFactory.Options options = new BitmapFactory.Options();
        options.inJustDecodeBounds = true; // 先获取原大小
        scanBitmap = BitmapFactory.decodeFile(path, options);
        options.inJustDecodeBounds = false; // 获取新的大小
        int sampleSize = (int) (options.outHeight / (float) 200);
        if (sampleSize <= 0)
            sampleSize = 1;
        options.inSampleSize = sampleSize;
        scanBitmap = BitmapFactory.decodeFile(path, options);
        RGBLuminanceSource source = new RGBLuminanceSource(scanBitmap);
        BinaryBitmap bitmap1 = new BinaryBitmap(new HybridBinarizer(source));
        QRCodeReader reader = new QRCodeReader();
        try {
            return reader.decode(bitmap1, hints);
        } catch (NotFoundException e) {
            e.printStackTrace();
        } catch (ChecksumException e) {
            e.printStackTrace();
        } catch (FormatException e) {
            e.printStackTrace();
        }
        return null;
    }

